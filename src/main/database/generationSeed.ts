export const GENERATION_KWH_BY_DATE: Record<string, HourlyGenerationKwh[]> = Object.fromEntries(
  Object.entries(GENERATION_KWH_VALUES_BY_DATE).map(([date, values]) => [
    date,
    values.map((kwh, hour) => ({
      hour,
      time: `${String(hour).padStart(2, '0')}:00`,
      kwh
    }))
  ])
)

export function getGenerationKwhByDate(date: string): HourlyGenerationKwh[] {
  return GENERATION_KWH_BY_DATE[date]?.map((item) => ({ ...item })) ?? []
}

export function getGenerationKwhTotal(date: string): number {
  const total = getGenerationKwhByDate(date).reduce((sum, item) => sum + item.kwh, 0)

  return Math.round(total * 1000) / 1000
}
